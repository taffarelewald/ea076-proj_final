/*Funcao de interrupcao temporal que faz uma varredura nos sensores para detectar o nivel de agua atual*/
void TI1_OnInterrupt(void)
{
	int water_measure[3] = {LEVEL1_GetVal(), LEVEL2_GetVal(), LEVEL3_GetVal()};		//vetor que contem o valor dos sensores para os 3 niveis de agua
	int i;
	
	/*loop que atribui nivel de agua atual Ã  variavel water_level, dando prioridade para niveis mais altos (reservatorio mais cheio)*/
	for(i = 2; i >= 0; i--) {
		if (!water_measure[i]) {
			water_level = i+1;
			break;
		}
		water_level = 0;
	}
}


